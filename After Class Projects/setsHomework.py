#SYMMETRIC DIFFERENCE IN STRINGS
greetings = {"Hello", "Welcome", "Hi"}
salutations = {"Hello", "How have you been", "BuhBye"}
print(greetings.symmetric_difference(salutations))

#SYMMETRIC DIFFERENCE IN NUMBERS
initialNums = {9, 2, 5, 7}
secondNums = [8, 3, 2, 6]
print(initialNums.symmetric_difference(secondNums))